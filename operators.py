c = 17
print(a<b and c>b)
print(a<b or c>a)
print( not a>b )

# Logical operators
x = True
y = False
print(x and y) # Returns true both conditions must be true(return false if at least one condition is false.)
print(x or y)  # Returns true if at least one condition is true(Returns false only if both conditions are false.)
print( not x ) # reverse

