# Arithmetic Operators
x = 15
y = 2
print(x + y)
print(x - y)
print(x * y)
print(x / y) # floot value 
print(x // y) # integer value (Floor division)
print(x % y) # reminder value (Modulo)
print(x ** y) # Exponentiation (Power of the number)


# Comparison operators
x = 30
y = 26
print(x > y)
print(x < y)
print(x == y)
print(x >= y)
print(x != y)
print(x <= y)

# Logical operators
a = 12
